const add = (a: number, b: number) => {
    return a + b;
}

const subtract = (a: number, b: number): number => { // arrow
    return a - b;
}

function divide(a: number, b: number):number { // named
    return a / b;
}

const multiply = function (a: number, b: number): number { // anonymous
    return a * b;
}

